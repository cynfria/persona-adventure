export const preloadImages = (imageSources) => {
  return Promise.all(
    imageSources.map((src) => {
      return new Promise((resolve, reject) => {
        const img = new Image();
        img.onload = resolve;
        img.onerror = resolve; // Resolve even on error to not block loading
        img.src = src;
      });
    })
  );
};
